function InitBizPhones()
	local bP = MySQL.query.await("SELECT id, number, muted FROM business_phones")
	for k, v in ipairs(bP) do
		_bizPhoneNumbers[v.id] = v.number
		_bizPhoneNumbersCheck[v.number] = v.id
		GlobalState[string.format("BizPhone:%s:Muted", v.id)] = v.muted
	end

	local trans = {}

	for k, v in pairs(_bizPhones) do
		if not _bizPhoneNumbers[v.id] then
			-- Needs Number Generating
			local number = Phone:GeneratePhoneNumber()
			v.number = number

			_bizPhoneNumbers[v.id] = v.number
			_bizPhoneNumbersCheck[v.number] = v.id

			table.insert(trans, {
				query = "INSERT INTO business_phones (id, number) VALUES (?, ?)",
				values = {
					v.id,
					number,
				},
			})
		else
			v.number = _bizPhoneNumbers[v.id]
		end
	end

	MySQL.transaction.await(trans)

	GlobalState.BizPhones = _bizPhones
end
